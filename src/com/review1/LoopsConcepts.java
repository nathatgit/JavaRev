package com.review1;

public class LoopsConcepts {

	public static void main(String[] args) {

		// 1. While Loop
		System.out.println("******WHILE LOOP******");

		int i = 1;
		while (i <= 10) {
			System.out.println(i);
			i++;
		}

		System.out.println("******FOR LOOP******");
		// 2. For Loop

		for (int j = 1; j <= 10; j++) {
			System.out.println(j);
		}
		for (int k = 10; k >= 1; k--)

		{
			System.out.println(k);

		}
	}

}