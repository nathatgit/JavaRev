package etcConcept;

public class ClassA {
	
	public ClassA() {
		System.out.println("Default Parent Class const");
	}
	
	public ClassA(int j)
	{
		System.out.println("Default Parent Class Const with Value of j is " + j);
	}
	
	public ClassA(int j, int i)
	{
		System.out.println("Default Parent Class Const with Value of j is " + j);
		System.out.println("Default Parent Class Const with Value of i is " + i);

	}

}
